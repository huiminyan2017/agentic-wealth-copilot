async def act5_copilot_conclusion(page: Page) -> None:
    """
    Act 5 — Copilot (Conclusion)
    Ask a plain-English question covering both tax and spending, then capture
    the chat interface showing the question and the AI answer together.
    """
    print("\nAct 5 — Copilot (conclusion)")

    # Navigate to root — Copilot Chat is the default page
    await page.goto(BASE, wait_until="networkidle")
    await page.wait_for_timeout(4000)

    # Shot 14: copilot answering a combined tax + spending question
    textarea = page.locator('[data-testid="stChatInput"] textarea')
    if await textarea.count():
        question = (
            f"For {DEMO_PERSON}: what are the tax optimisation suggestions "
            "based on their income, and what spending categories should they "
            "cut back on to improve savings?"
        )
        await textarea.click()
        await textarea.fill(question)
        await page.wait_for_timeout(300)

        # Click the submit arrow button
        submitted = False
        for selector in [
            '[data-testid="stChatInput"] button[type="submit"]',
            '[data-testid="stChatInputSubmitButton"]',
            '[data-testid="stChatInput"] button',
        ]:
            btn = page.locator(selector)
            if await btn.count():
                await btn.first.click()
                submitted = True
                break

        if not submitted:
            await textarea.press("Enter")

        # Wait for the LangGraph agent to respond (may take up to 30 s)
        await page.wait_for_timeout(30000)
        await scroll_to(page, 0)          # scroll to top so question is visible
        await shot(page, "16_copilot_chat.png")
    else:
        print("  (chat input not found — skipping copilot shot)")
        await shot(page, "16_copilot_chat.png")


# ── main ──────────────────────────────────────────────────────────────────────

async def main() -> None:
    if OUT_DIR.exists():
        shutil.rmtree(OUT_DIR)
    OUT_DIR.mkdir(parents=True)

    print(f"Output → {OUT_DIR}")
    print("Story: Income pipeline → Spending → Wealth → Investing → Copilot (conclusion)\n")

    async with async_playwright() as pw:
        browser = await pw.chromium.launch(headless=True)
        ctx     = await browser.new_context(viewport=VIEWPORT)
        page    = await ctx.new_page()

        await act1_income_pipeline(page)
        await act2_spending(page)
        await act3_wealth(page)
        await act4_investing(page)
        await act5_copilot_conclusion(page)

        await browser.close()

    count = len(list(OUT_DIR.glob("*.png")))
    print(f"\n✅  Done — {count}/16 screenshots in {OUT_DIR}")
    print("    (shots 05-06 are static — run scripts/generate_json_schema_screenshot.py separately)")


if __name__ == "__main__":
    asyncio.run(main())
